// Helper function to get a random profile image
export const getRandomProfileImage = (type: 'users' | 'communities' = 'users') => {
    const images = PROFILE_IMAGES[type];
    return images[Math.floor(Math.random() * images.length)];
};

// Helper function to get a random community cover image
export const getRandomCommunityCover = () => {
    const images = PROFILE_IMAGES.communityCovers;
    return images[Math.floor(Math.random() * images.length)];
};

// Helper function to get a random avatar for an agent category
export const getAgentAvatar = (category: string, agentId?: string): string => {
    const categoryKey = category.toLowerCase() as keyof typeof AGENT_AVATARS;
    const avatars = AGENT_AVATARS[categoryKey] || AGENT_AVATARS.default;
    
    if (agentId) {
        // Use agent ID to consistently select the same avatar for the same agent
        const index = Math.abs(agentId.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0)) % avatars.length;
        return avatars[index];
    }
    
    // Return random avatar if no agent ID provided
    const randomIndex = Math.floor(Math.random() * avatars.length);
    return avatars[randomIndex];
}; 